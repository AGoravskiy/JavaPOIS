package bsujavalab04b9;

public class Logic {
    public static int calculateSquareIn(double a, double b, double c){
        
        return (int) ((int)(b/a) * (int)(c/a));
    }
    
    public static double calculateSquareSpace(double a){
        
        return a*a;
    }
    
    
    public static double calculateRectangleSpace(double b, double c){
        
        return b*c;
    }
}
